                <h5>What is Accreditation?</h5>
                <p>Accreditation is viewed as a process by which an institution at the tertiary level evaluates its educational activities, in whole or in part, and seeks an independent judgment to confirm that it substantially achieves its objectives, and is generally equal in quality to comparable institutions.</p>
