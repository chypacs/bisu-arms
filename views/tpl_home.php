                <h2> What is AACCUP? </h2>
                <br>
                <div class="col-lg-13 ms-2 d-flex flex-column justify-content-center">
                    <p>The accreditation of curricular programs in the Philippines, particularly for state universities and colleges, is the main function of the Accrediting Agency of Chartered Colleges and Universities in the Philippines (AACCUP), Inc. Organized in 1987, though officially registered and recognized under the Securities and Exchange Commission (SEC) on September 4, 1989, it is the youngest of the four (4) accrediting agencies in the country until late 2003. Under its charter, one of the functions, if not the main purpose of AACCUP, is "to develop a mechanism of, and conduct the evaluation of programs and institutions."</p>
                    <br/>
                    <p>AACCUP is now closely allied with the Association of Local Colleges and Universities Commission on Accreditation, Inc. (ALCUCOA), organized only in the later part of year 2003.</p>
                </div>
                <h3> Vision </h3>
                <p>To develop a mechanism for, and conduct the evaluation of programs and institutions.</p>
                <br/>
                <h3>Mission</h3>
                <p>To make the attainment of quality in education an integral part of the higher education systems more particularly among chartered state colleges and universities in the Philippines, through a sustained program of internal and external assessment.</p>
                <br/>
                <h2>The Program of Accreditation </h2>
                <br/>
                <h5>What is Accreditation?</h5>
                <p>Accreditation is viewed as a process by which an institution at the tertiary level evaluates its educational activities, in whole or in part, and seeks an independent judgment to confirm that it substantially achieves its objectives, and is generally equal in quality to comparable institutions.</p>
                <br/>
                <h5>Program As the Unit of Assessment </h5>
                <p>Currently, accreditation in state colleges and universities is by program. A program is defined as a course or a group of related courses packaged in a curriculum and leading to a graduate or undergraduate degree. Examples of programs are elementary teacher education, civil engineering, agriculture, etc.</p>
                <br/>
                <p>AACCUP is now considering other models, like, accrediting by institution as alternatives to, or to complement program accreditation.</p>
                <br/>
                <h5>Other Attributes </h5>
                <br/>
                <p>Aside from being program-focused, accreditation is:</p>
                <ol type="1">
                    <li class="ms-3">based on standards of the accrediting agency, which are normally higher than those set by the Commission on Higher Education (CHED) and other appropriate agencies, e.g. Professional Regulation Commission (PRC).;</li>
                    <li class="ms-3">voluntary on the part of the higher education institution that may want to be accredited;</li>
                    <li class="ms-3">an evaluation by peers, i. e. the external accreditors are mostly faculty members from other higher education institutions; and</li>
                    <li class="ms-3">non-governmental.</li>
                </ol>
                <h5>The Outcomes-Based Quality Assurance</h5>
                <ol type="1">
                    <li class="ms-3">To us in AACCUP, the current strongest factor to harmonize QA practices, and the best means to promote a level playing field, is the CHED-initiated Outcomes-Based Quality Assurance System (CMO 46).</li>
                    <li class="ms-3">Upon the invitation of CHED, and as accepted by the AACCUP Board of Trustees, a one-year CHED-AACCUP contract was crafted in 2014, with CHED providing funding assistance amounting to P2 Million for AACCUP to revise its instruments of program and institutional accreditation “in line with outcomes-based quality assurance…” (AACCUP completed the one-year contract on time on September 30, 2015.
                    </li>
                    <li class="ms-3">Tapping the services of AACCUP Officials and Senior Accreditors, Academic Program Specialists, the AACCUP Pool of Institutional Accreditors and International Experts, the following sequence of steps was pursued:
                        <ol type="A">
                            <li class="ms-4">Formulation of AACCUP QA Framework aligned to CHED, by top-AACCUP officials.</li>
                            <li class="ms-4">Preparation of the Master Survey Instrument by top AACCUP officials and a pool of Senior Accreditors</li>
                            <li class="ms-4">Preparation of the 42 Outcomes-Based Program Accreditation Instruments by 42 Curricular Program Specialists</li>
                            <li class="ms-4">Review of the draft Outcomes-Based Program Accreditation Instruments by a small group of Senior Accreditors</li>
                            <li class="ms-4">Review, in a national workshop, of the draft Instruments</li>
                            <li class="ms-4">Finalization of the Instruments, and Preparation of the Guidelines in preparing the Program Performance Profile (PPP) and Institutional Portfolio (IP)</li>
                            <li class="ms-4">Validation of the Instruments</li>
                            <li class="ms-4">Copyrighting of all the Instruments</li>
                        </ol>
                    </li>
                    <li class="ms-3">Output of the Contract:
                        <ol type="A">
                            <li class="ms-4">One (1) Instrument on Institutional Accreditation</li>
                            <li class="ms-4">Forty-two (42) Instruments on forty-two (42) Program Accreditation</li>
                            <li class="ms-4">One (1) Guidelines in preparing the Outcomes-Based Program Performance Profile</li>
                            <li class="ms-4">One (1) Guidelines in preparing the Institutional Portfolio</li>
                        </ol>
                    </li>
                    <li class="ms-3">The AACCUP Outcomes-Based Framework is aligned to the CHED policy of defining Quality as “exceptional” i.e., exceeding very high standards as against Quality as “fitness of purpose” and/or “developing a culture of quality”.</li>
                    <li class="ms-3">The accreditation instruments are also aligned to the definition of outcomes-based Quality Assurance as measurement of , but still consider inputs and processes as important. Thus, AACCUP measurement of quality of programs and institutions include:
                        <ol type="A">
                            <li class="ms-4">System (inputs and processes)</li>
                            <li class="ms-4">Implementation</li>
                            <li class="ms-4">Outcomes</li>
                        </ol>
                        <br/>
                        AACCUP evaluation also gives premium to “best practices”.
                    </li>
